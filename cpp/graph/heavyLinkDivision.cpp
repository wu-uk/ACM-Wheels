int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int n, m, s;
    cin >> n >> m >> s;
    HLD hld(n);    
    for (int i = 0; i < n - 1; ++i) {
        int u, v;
        cin >> u >> v;
        hld.addEdge(u - 1, v - 1);
    }
    hld.work(s - 1);
    for (int i = 0; i < m; ++i) {
        int x, y;
        cin >> x >> y;
        cout << hld.lca(x - 1, y - 1) + 1 << "\n";
    }
}